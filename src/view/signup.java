    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // yeta ho
        String firstname = tfFirstName.getText();
        String last_name = tfLastName.getText();
        String phonenum = tfPhonenumber.getText();
        String username = tfusername.getText();
        String password = tfpassword.getText();
        String cPassword = tfconfpassword.getText();
        Users newUser = new Users(firstname,last_name,phonenum,username,password,cPassword);
        UsersController uc = new UsersController();
        int result = uc.registerUser(newUser);
        if(result>0){
            JOptionPane.showMessageDialog(this, "User Registered Succesfully","Success",JOptionPane.INFORMATION_MESSAGE);
        } else{
                    JOptionPane.showMessageDialog(this, "Failed to register user","Error",JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

